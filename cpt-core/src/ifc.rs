/// Render an IFCX-style JSON document. Uses a flat object list with a
/// stable schema string (`ifcx-cpt-0.1`) that the IfcView frontend can
/// recognise. This is *not* the official IFCX schema (still in flux);
/// it borrows the IFCX conventions (header + flat objects + typed kind
/// strings) so future migration to real IFCX is straightforward.
pub fn write_ifcx(cpts: &[Cpt], project: &ProjectMeta) -> String {
    #[derive(Serialize)]
    struct IfcxFile {
        schema: &'static str,
        header: Header,
        objects: Vec<Value>,
    }
    #[derive(Serialize)]
    struct Header {
        originating_system: &'static str,
        timestamp: String,
        note: &'static str,
    }

    let mut objects: Vec<Value> = Vec::with_capacity(cpts.len() + 2);

    objects.push(json!({
        "type": "IfcProject",
        "id": pseudo_guid("project", &project.title),
        "name": project.title,
        "client": project.client,
        "location": project.location,
        "project_number": project.project_number,
        "author": project.author,
        "date": project.date.format("%Y-%m-%d").to_string(),
    }));

    objects.push(json!({
        "type": "IfcSite",
        "id": pseudo_guid("site", &project.location),
        "name": format!("Site - {}", project.location),
    }));

    for cpt in cpts {
        let position = cpt.position.map(|p| {
            json!({
                "x_rd": p.x_rd,
                "y_rd": p.y_rd,
                "z_nap": p.z_nap,
            })
        });
        let points: Vec<Value> = cpt
            .points
            .iter()
            .map(|p| {
                json!({
                    "depth": p.depth,
                    "depth_nap": p.depth_nap,
                    "qc": p.qc,
                    "fs": p.fs,
                    "rf": p.rf,
                    "u2": p.u2,
                    "inclination": p.inclination,
                })
            })
            .collect();

        objects.push(json!({
            "type": "OpenGeoCpt",
            "id": cpt.id,
            "guid": pseudo_guid("cpt", &cpt.id),
            "source_file": cpt.metadata.source_file,
            "project_name": cpt.metadata.project_name,
            "project_number": cpt.metadata.project_number,
            "date": cpt.metadata.date.map(|d| d.format("%Y-%m-%d").to_string()),
            "ground_level_nap": cpt.metadata.ground_level_nap,
            "position": position,
            "points": points,
        }));
    }

    let file = IfcxFile {
        schema: IFCX_SCHEMA,
        header: Header {
            originating_system: "Open Geotechniek Studio",
            timestamp: chrono::Utc::now().to_rfc3339(),
            note: IFC_HEADER_NOTE,
        },
        objects,
    };

    serde_json::to_string_pretty(&file)
        .unwrap_or_else(|_| "{\"schema\":\"ifcx-cpt-0.1\",\"objects\":[]}".to_string())
}

