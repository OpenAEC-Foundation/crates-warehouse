//! IFC writers — produce IFC4x3 (STEP) and IFCX (JSON) representations of
//! a CPT project.
//!
//! **These writers are intentionally placeholders.** The output is meant to
//! *look* like IFC4x3 STEP / IFCX JSON and to embed the project + CPT data
//! in a way a future, schema-aware IFC tool could parse. They are not
//! validated against the official IFC4x3 schema, and they will not load
//! cleanly into Bonsai/IfcOpenShell. The intent is to give Open Geotechniek
//! Studio a tangible "IFC export" surface today, while leaving the heavy
//! lift (a real IFC4x3 mapping for geotechnical entities) to a separate
//! `cpt-ifc` crate later on.
//!
//! Both writers consume `&[Cpt]` plus a `ProjectMeta` (re-using the
//! `report::ProjectMeta` already shipped to the Tauri layer).

use std::fmt::Write as _;

use serde::Serialize;
use serde_json::{json, Value};

use crate::domain::Cpt;
use crate::report::ProjectMeta;

const IFC_HEADER_NOTE: &str =
    "Generated by Open Geotechniek Studio — placeholder structure (not schema-valid IFC4x3)";
const IFCX_SCHEMA: &str = "ifcx-cpt-0.1";

// ───────────────────────────────────────────────────────────────────
// IFC4x3 (STEP / Part 21) writer
// ───────────────────────────────────────────────────────────────────

/// Render a STEP-format IFC4x3 text file containing the project metadata
/// and one entity per CPT, with measurement data exposed as IfcPropertySet
/// entries (depth / qc / fs / rf as IfcPropertyListValue).
///
/// Output is plain ASCII STEP — newline-terminated, instance numbers start
/// at `#1` for the project, with subsequent ids allocated in stride.
pub fn write_ifc4x3(cpts: &[Cpt], project: &ProjectMeta) -> String {
    let mut out = String::new();
    let timestamp = chrono::Utc::now().format("%Y-%m-%dT%H:%M:%S").to_string();
    let project_guid = pseudo_guid("project", &project.title);

    // Header — Part 21 envelope.
    out.push_str("ISO-10303-21;\n");
    out.push_str(&format!("/* {} */\n", IFC_HEADER_NOTE));
    out.push_str("HEADER;\n");
    out.push_str("FILE_DESCRIPTION(('Open Geotechniek Studio CPT export'),'2;1');\n");
    out.push_str(&format!(
        "FILE_NAME('{}','{}',('{}'),('{}'),'Open Geotechniek Studio','cpt-core ifc writer','');\n",
        step_str(&format!("{}.ifc", safe_filename(&project.title))),
        timestamp,
        step_str(&project.author),
        step_str(&project.client),
    ));
    out.push_str("FILE_SCHEMA(('IFC4X3'));\n");
    out.push_str("ENDSEC;\n");
    out.push_str("DATA;\n");

    // Static infrastructure entities (ids #1..#20).
    writeln!(
        out,
        "#1 = IFCPROJECT('{guid}',$,'{name}','{desc}',$,$,$,(#10),#5);",
        guid = project_guid,
        name = step_str(&project.title),
        desc = step_str(&format!(
            "{} - {} ({})",
            project.client, project.location, project.project_number
        )),
    )
    .ok();
    out.push_str("#5 = IFCUNITASSIGNMENT((#6,#7));\n");
    out.push_str("#6 = IFCSIUNIT(*,.LENGTHUNIT.,$,.METRE.);\n");
    out.push_str("#7 = IFCSIUNIT(*,.PRESSUREUNIT.,.MEGA.,.PASCAL.);\n");
    out.push_str("#10 = IFCGEOMETRICREPRESENTATIONCONTEXT($,'Model',3,1.0E-5,#11,$);\n");
    out.push_str("#11 = IFCAXIS2PLACEMENT3D(#12,$,$);\n");
    out.push_str("#12 = IFCCARTESIANPOINT((0.,0.,0.));\n");
    writeln!(
        out,
        "#20 = IFCSITE('{guid}',$,'{name}',$,$,$,$,$,$,$,$,$,$,$);",
        guid = pseudo_guid("site", &project.location),
        name = step_str(&format!("Site - {}", project.location)),
    )
    .ok();

    // Per-CPT entities. Each CPT gets:
    //   - one IFCBUILDINGELEMENTPROXY (stand-in for a future
    //     IfcGeotechnicalElement / IfcBorehole subtype)
    //   - an IfcLocalPlacement positioned at its RD coordinates
    //   - an IfcPropertySet holding the measurement points as
    //     IfcPropertyListValue entries (depth / qc / fs / rf vectors)
    //   - one IfcRelDefinesByProperties tying the property set to the proxy
    let mut next_id: usize = 100;
    for cpt in cpts {
        let cpt_id = next_id;
        let placement_id = next_id + 1;
        let axis_id = next_id + 2;
        let point_id = next_id + 3;
        let pset_id = next_id + 4;
        let pdepth_id = next_id + 5;
        let pqc_id = next_id + 6;
        let pfs_id = next_id + 7;
        let prf_id = next_id + 8;
        let rel_id = next_id + 9;
        next_id += 10;

        let (x, y, z) = cpt
            .position
            .map(|p| (p.x_rd, p.y_rd, p.z_nap.unwrap_or(0.0)))
            .unwrap_or((0.0, 0.0, 0.0));

        writeln!(
            out,
            "#{point} = IFCCARTESIANPOINT(({x:.3},{y:.3},{z:.3}));",
            point = point_id,
            x = x,
            y = y,
            z = z,
        )
        .ok();
        writeln!(out, "#{} = IFCAXIS2PLACEMENT3D(#{},$,$);", axis_id, point_id).ok();
        writeln!(
            out,
            "#{place} = IFCLOCALPLACEMENT($,#{axis});",
            place = placement_id,
            axis = axis_id,
        )
        .ok();
        writeln!(
            out,
            "#{cid} = IFCBUILDINGELEMENTPROXY('{guid}',$,'{name}','{desc}',$,#{place},$,$,$);",
            cid = cpt_id,
            guid = pseudo_guid("cpt", &cpt.id),
            name = step_str(&cpt.id),
            desc = step_str(&format!(
                "CPT - {} points, depth {:.2} m",
                cpt.points.len(),
                cpt.points.iter().map(|p| p.depth).fold(0.0_f64, f64::max),
            )),
            place = placement_id,
        )
        .ok();

        // Property values — the four measurement vectors.
        writeln!(
            out,
            "#{} = IFCPROPERTYLISTVALUE('Depth',$,({}),IFCLENGTHMEASURE);",
            pdepth_id,
            cpt.points
                .iter()
                .map(|p| format!("IFCLENGTHMEASURE({:.3})", p.depth))
                .collect::<Vec<_>>()
                .join(","),
        )
        .ok();
        writeln!(
            out,
            "#{} = IFCPROPERTYLISTVALUE('Qc',$,({}),IFCPRESSUREMEASURE);",
            pqc_id,
            opt_list(cpts_iter_qc(cpt), |v| format!("IFCPRESSUREMEASURE({:.4})", v)),
        )
        .ok();
        writeln!(
            out,
            "#{} = IFCPROPERTYLISTVALUE('Fs',$,({}),IFCPRESSUREMEASURE);",
            pfs_id,
            opt_list(cpts_iter_fs(cpt), |v| format!("IFCPRESSUREMEASURE({:.4})", v)),
        )
        .ok();
        writeln!(
            out,
            "#{} = IFCPROPERTYLISTVALUE('Rf',$,({}),IFCREAL);",
            prf_id,
            opt_list(cpts_iter_rf(cpt), |v| format!("IFCREAL({:.4})", v)),
        )
        .ok();
        writeln!(
            out,
            "#{pset} = IFCPROPERTYSET('{guid}',$,'OpenGeo_CptMeasurements',$,(#{pd},#{pq},#{pf},#{pr}));",
            pset = pset_id,
            guid = pseudo_guid("pset", &cpt.id),
            pd = pdepth_id,
            pq = pqc_id,
            pf = pfs_id,
            pr = prf_id,
        )
        .ok();
        writeln!(
            out,
            "#{rel} = IFCRELDEFINESBYPROPERTIES('{guid}',$,$,$,(#{cid}),#{pset});",
            rel = rel_id,
            guid = pseudo_guid("rel", &cpt.id),
            cid = cpt_id,
            pset = pset_id,
        )
        .ok();
    }

    out.push_str("ENDSEC;\n");
    out.push_str("END-ISO-10303-21;\n");
    out
}

