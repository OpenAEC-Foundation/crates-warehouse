// Helpers
// ───────────────────────────────────────────────────────────────────

/// Build a deterministic pseudo-GUID string from a kind label and a
/// stable input. Not a real IFC GlobalId (which is base64), just a
/// readable placeholder so two runs with the same input produce the
/// same id.
fn pseudo_guid(kind: &str, key: &str) -> String {
    let mut h: u64 = 0xcbf2_9ce4_8422_2325;
    for b in kind.bytes().chain(b"|".iter().copied()).chain(key.bytes()) {
        h ^= b as u64;
        h = h.wrapping_mul(0x100_0000_01b3);
    }
    format!("OGS{:016x}{:08x}", h, h.rotate_left(13) as u32)
}

/// Escape a string for inclusion inside a STEP single-quoted literal.
/// STEP requires `'` doubled and backslashes escaped — we do the
/// minimum-viable subset (single quote + control char) to avoid producing
/// obviously broken output.
fn step_str(s: &str) -> String {
    let mut out = String::with_capacity(s.len());
    for ch in s.chars() {
        match ch {
            '\'' => out.push_str("''"),
            '\\' => out.push_str("\\\\"),
            c if (c as u32) < 0x20 => {} // strip control chars
            c => out.push(c),
        }
    }
    out
}

fn safe_filename(s: &str) -> String {
    s.chars()
        .map(|c| if c.is_ascii_alphanumeric() || c == '-' || c == '_' { c } else { '_' })
        .collect()
}

fn cpts_iter_qc(cpt: &Cpt) -> impl Iterator<Item = Option<f64>> + '_ {
    cpt.points.iter().map(|p| p.qc)
}
fn cpts_iter_fs(cpt: &Cpt) -> impl Iterator<Item = Option<f64>> + '_ {
    cpt.points.iter().map(|p| p.fs)
}
fn cpts_iter_rf(cpt: &Cpt) -> impl Iterator<Item = Option<f64>> + '_ {
    cpt.points.iter().map(|p| p.rf)
}

/// Render a list of `Option<f64>` measurements into a comma-separated
/// IFC value list. `None` values are emitted as `$` (STEP unset).
fn opt_list<I, F>(iter: I, render: F) -> String
where
    I: Iterator<Item = Option<f64>>,
    F: Fn(f64) -> String,
{
    iter.map(|v| match v {
        Some(x) => render(x),
        None => "$".to_string(),
    })
    .collect::<Vec<_>>()
    .join(",")
}

#[cfg(test)]
mod tests {
    use super::*;
    use crate::domain::{MeasurementPoint, Metadata, Position};
    use chrono::NaiveDate;

    fn sample_cpt(id: &str) -> Cpt {
        Cpt {
            id: id.to_string(),
            metadata: Metadata {
                source_file: format!("{id}.gef"),
                ..Default::default()
            },
            position: Some(Position {
                x_rd: 100_000.0,
                y_rd: 400_000.0,
                z_nap: Some(2.5),
            }),
            points: vec![
                MeasurementPoint {
                    depth: 0.02,
                    depth_nap: Some(2.48),
                    qc: Some(1.5),
                    fs: Some(0.015),
                    rf: Some(1.0),
                    u2: None,
                    inclination: None,
                },
                MeasurementPoint {
                    depth: 0.04,
                    depth_nap: Some(2.46),
                    qc: Some(1.6),
                    fs: Some(0.016),
                    rf: Some(1.0),
                    u2: None,
                    inclination: None,
                },
            ],
        }
    }

    fn sample_project() -> ProjectMeta {
        ProjectMeta {
            title: "Test project".into(),
            client: "ACME bv".into(),
            location: "Amsterdam".into(),
            project_number: "2026-001".into(),
            author: "Open Geotechniek Studio".into(),
            date: NaiveDate::from_ymd_opt(2026, 5, 15).unwrap(),
        }
    }

    #[test]
    fn ifc4x3_smoke() {
        let cpts = vec![sample_cpt("S01"), sample_cpt("S02")];
        let s = write_ifc4x3(&cpts, &sample_project());
        assert!(s.contains("ISO-10303-21"), "missing STEP envelope");
        assert!(
            s.contains("FILE_SCHEMA(('IFC4X3'))"),
            "missing IFC4X3 schema declaration"
        );
        assert!(s.contains("'S01'"), "missing CPT id S01 in output");
        assert!(s.contains("'S02'"), "missing CPT id S02 in output");
        assert!(s.contains("END-ISO-10303-21"), "missing STEP terminator");
        assert!(
            s.contains("OpenGeo_CptMeasurements"),
            "missing measurement property set",
        );
    }

    #[test]
    fn ifc4x3_handles_empty_cpts() {
        let s = write_ifc4x3(&[], &sample_project());
        assert!(s.contains("FILE_SCHEMA(('IFC4X3'))"));
        assert!(s.contains("IFCPROJECT"));
        assert!(s.contains("END-ISO-10303-21"));
    }

    #[test]
    fn ifcx_smoke() {
        let cpts = vec![sample_cpt("S01")];
        let s = write_ifcx(&cpts, &sample_project());
        assert!(s.contains("\"schema\": \"ifcx-cpt-0.1\""));
        assert!(s.contains("\"OpenGeoCpt\""));
        assert!(s.contains("\"S01\""));
        // Re-parsable as JSON.
        let v: Value = serde_json::from_str(&s).unwrap();
        assert_eq!(v["schema"], "ifcx-cpt-0.1");
    }
}
